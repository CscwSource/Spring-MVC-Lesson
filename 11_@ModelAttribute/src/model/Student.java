package model;

public class Student
{
	//学校
	private String school;
	
	//学号
	private Long stuid;

	
	
	public String getSchool()
	{
		return school;
	}

	public void setSchool(String school)
	{
		this.school = school;
	}

	public Long getStuid()
	{
		return stuid;
	}

	public void setStuid(Long stuid)
	{
		this.stuid = stuid;
	}
	

}
